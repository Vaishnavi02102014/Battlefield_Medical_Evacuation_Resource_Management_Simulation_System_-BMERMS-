def _base_layout(figure: go.Figure) -> go.Figure:
    figure.update_layout(
        margin=dict(l=10, r=10, t=10, b=10),
        height=CHART_HEIGHT_PX,
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        font_color=COLOR_TEXT_SECONDARY,
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
    )
    return figure
 
 
def _build_severity_chart(data: dict) -> go.Figure:
    labels = list(data.keys())
    values = list(data.values())
    colors = [SEVERITY_COLOR.get(label, COLOR_INFO) for label in labels]
    figure = go.Figure(go.Bar(x=labels, y=values, marker_color=colors, text=values, textposition="outside"))
    figure = _base_layout(figure)
    figure.update_yaxes(showgrid=True, gridcolor="rgba(255,255,255,0.06)")
    figure.update_xaxes(showgrid=False)
    return figure
 
 
def _build_incident_distribution_chart(data: dict) -> go.Figure:
    items = sorted(data.items(), key=lambda kv: kv[1])
    labels = [k for k, _ in items]
    values = [v for _, v in items]
    figure = go.Figure(go.Bar(
        x=values, y=labels, orientation="h",
        marker_color=COLOR_DANGER, text=values, textposition="outside",
    ))
    figure = _base_layout(figure)
    figure.update_xaxes(showgrid=True, gridcolor="rgba(255,255,255,0.06)")
    figure.update_yaxes(showgrid=False)
    return figure
 
 
def _build_facility_utilization_trend_chart(data: dict) -> go.Figure:
    palette = [COLOR_PRIMARY, COLOR_INFO, COLOR_WARNING, COLOR_DANGER]
    figure = go.Figure()
    for color, (facility, values) in zip(palette, data["series"].items()):
        figure.add_trace(go.Scatter(
            x=data["days"], y=values, name=facility,
            mode="lines+markers", line=dict(color=color, width=2),
        ))
    figure = _base_layout(figure)
    figure.update_yaxes(showgrid=True, gridcolor="rgba(255,255,255,0.06)", ticksuffix="%")
    figure.update_xaxes(showgrid=False)
    return figure
 
 
