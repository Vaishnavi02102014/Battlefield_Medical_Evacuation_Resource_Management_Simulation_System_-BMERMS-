"""
reports.py
 
Reports page — Phase 3 (final UI refinement, pre-backend-integration
polish). Historical review, report generation, and export surface for
BMERMS.
 
Answers: "What happened during this operation, and how can commanders
review or export it?" — NOT "what is happening right now?" (that's
Dashboard/Analytics). Accordingly this page deliberately does NOT
duplicate:
    Dashboard  -> live operational snapshot
    Simulation -> real-time control
    Facilities -> per-facility console
    Patients   -> casualty registry / live case management
    Resources  -> live fleet inventory
    Analytics  -> live trend / forecast / AI decision support
 
PHASE 3 CHANGES (this file only — UI polish, no backend, no other page)
    1. constants.BATTLE_SECTORS is imported defensively: if it's ever
       removed from constants.py, REPORT_SECTORS falls back to a local
       Alpha-Delta list instead of raising ImportError at import time.
    2. Mock "generated by" values are now role codes (OPS-OFFICER,
       MED-COMMAND, DUTY-OFFICER, SYSTEM) instead of invented named
       officers, since no personnel/identity table exists yet.
    3. Every mock report now carries a report_id (RPT-0xx).
    4. Every report row shows a bracketed tactical type tag ([CAS],
       [FAC], [RES], [INC], [TRT], [SIT]) reusing the same
       TEMPLATE_BADGE mapping Report Templates uses.
    5. Report Metadata strip: "Filters Applied" moved to its own
       full-width row with explicit word-wrap styling, so long filter
       strings wrap cleanly instead of overflowing or breaking mid-word
       inside a narrow column.
    6. "Export Queue" renamed to "Queued Reports" everywhere (tab, KPI
       card, data function, stats strip).
    7. All three report tabs (Recent / Queued / Archived) now show a
       distinct, styled empty state instead of a bare caption.
    8. A compact report-statistics strip (Completed / Queued / Archived
       / Failed counts) sits above the report tabs.
    9. Report Preview shows a professional placeholder message until a
       template or the builder has generated at least one report in this
       session; only then do the metadata strip + Summary/Tables/Charts
       tabs appear.
    10. A small classification badge ("CLASSIFICATION: OPERATIONAL USE")
        sits at the top of the page, reusing the existing
        .medevac-status-badge class already defined in theme.css (no CSS
        file touched).
 
BACKEND STATUS (unchanged)
    No Reports table exists in the schema yet (see constants.py /
    models.py — there is no Report/ReportTemplate dataclass today), and
    the backend currently only supports CSV export, not report generation
    or persistence. Every data function below therefore returns
    placeholder data shaped like its eventual real source, with a
    "TODO Integration" docstring naming the future crud.py function (or
    schema addition) that will replace it. No function here reads or
    writes the SQLite database — this file has zero backend wiring.
 
    Report IDs, role-code attribution, and status counts are all
    UI-only/mock for now — none of them read a real identity, job-queue,
    or persistence layer, none of which exist backend-side yet either.
 
Explicitly out of scope (per spec) even as placeholders: PDF export,
Excel export, email delivery, AI-generated narrative summaries. Only CSV
export placeholders appear, matching current backend capability.
"""
 
