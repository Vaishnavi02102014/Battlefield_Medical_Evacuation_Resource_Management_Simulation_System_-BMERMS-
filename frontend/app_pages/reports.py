def _compute_filters_applied_label() -> str:
    filter_keys = {
        "Facility": "reports_filter_facility",
        "Severity": "reports_filter_severity",
        "Incident Type": "reports_filter_incident_type",
        "Sector": "reports_filter_sector",
        "Status": "reports_filter_status",
        "Operation": "reports_filter_operation",
    }
    applied = [
        f"{label}: {st.session_state[key]}"
        for label, key in filter_keys.items()
        if st.session_state.get(key, "All") != "All"
    ]
    return "; ".join(applied) if applied else "No filters applied"
 
 
