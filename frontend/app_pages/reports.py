def _render_report_table(reports: list[dict], time_label: str, key_prefix: str, empty_message: str) -> None:
    if not reports:
        _render_empty_state(empty_message)
        return
 
    col_widths = [1.1, 3.3, 1.7, 1.6, 1.3, 1.1]
    header = st.columns(col_widths)
    header[0].caption("ID")
    header[1].caption("Report Name")
    header[2].caption("Generated By")
    header[3].caption(time_label)
    header[4].caption("Status")
    header[5].caption("Export")
 
    for i, report in enumerate(reports):
        row = st.columns(col_widths)
        row[0].markdown(_render_report_id_badge(report["report_id"]), unsafe_allow_html=True)
        row[1].markdown(
            f'{_type_tag_html(report["report_type"])}<strong>{report["name"]}</strong>',
            unsafe_allow_html=True,
        )
        row[2].caption(report["generated_by"])
        row[3].caption(report["generated_time"])
        color = _ACCENT.get(REPORT_STATUS_COLOR.get(report["status"], "info"), COLOR_INFO)
        row[4].markdown(
            f'<span style="color:{color};font-weight:600;font-size:0.82rem;">{report["status"]}</span>',
            unsafe_allow_html=True,
        )
        with row[5]:
            # TODO Integration: enable once crud.export_report_csv(report_id)
            # exists backend-side (backend currently supports CSV export
            # only — no PDF/Excel/email, per spec). Placeholder only.
            st.button("⬇ CSV", key=f"{key_prefix}_csv_{i}", disabled=True, help="CSV export — backend integration pending")
        if i < len(reports) - 1:
            st.divider()
 
 
